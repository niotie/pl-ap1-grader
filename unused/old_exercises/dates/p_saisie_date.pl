title=Saisie d'une date

text== 

Écrire un programme demandant à l'utilisateur de saisir au clavier trois
entiers correspondant à un jour, un mois et une année et les stockant dans
trois variables respectivement appelées `jour`, `mois` et `annee`. De plus, le
programme affichera exactement le texte `La date choisie est J/M/A` (où `J`,
`M` et `A` sont respectivement la valeur du jour, du mois et de l'année saisis,
sans formatage particulier).

